public class Day3 {

    public static void main(String[] args) {

        //Assignment operators: (+=,-=,*=,/=,%=) used in for loop
        int a=10;
        System.out.println("a:"+a);

        a+=20;
        System.out.println("a:"+a);

        a-=5;
        System.out.println("a:"+a);

        a*=4;
        System.out.println("a:"+a);

        a/=8;
        System.out.println("a:"+a);

        a%=5;
        System.out.println("a:"+a);
        //Bitwise Operator:
        //AND Opertator &
        System.out.println(12 & 4);
        //OR Operator | :
        System.out.println(12 | 3);
        
        //XOR Operator ^:
        System.out.println(10 ^ 4);
    }
}